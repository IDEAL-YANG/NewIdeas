对于2016年最火的Prisma来说，跟我们了一个讯息，深度学习对于处理图片的应用，必然带来革命性的改变。针对这个变化简述如下：

首先拿一张特征图，很多教程里都是以梵高的星空🌃为例，和一张内容图，通过训练创建检查点文件。这个训练需要很长时间，GPU或相对CPU快一些。
然后生成一个pd后缀文件，可以优化这个文件，已占用更小的存储，形成快速神经风格迁移模型。

之后，用这个模型与任意一个图片，进行融合，生成一个结果图。

我的想法是，这个结果图是否存在一个其他唯一反模型，使得结果图还原成原图，这个唯一的反模型和原来的模型组成一组密钥对，共同保证一个图片的更安全传输。
使用这种密钥对，既可以直观的使用结果图，又不会被非法使用。